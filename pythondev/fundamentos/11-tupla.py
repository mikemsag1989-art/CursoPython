filmTuple =("Inception", "The Shawshank Redemption", "The Dark Knight", "Pulp Fiction")
print (type(filmTuple))

#1 - Buscar os 2 primeiros filmes da tupla
print (filmTuple[0:2])

#2 - Buscar o último filme da tupla
print (filmTuple[-1])   

#3 - Buscar filmes até uma determinada posição
print (filmTuple[:3])   

#4 - Buscar filmes de uma posição em diante
print (filmTuple[2:])  

#5 - Recupera o índice de um item da tupla
print (filmTuple.index("The Dark Knight"))  
